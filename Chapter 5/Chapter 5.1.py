x = float(input("Give me a number"))
          
if x % 2 == 0:
    print(x, " is even.")
    print("FUN FACT ABOUT EVEN NUMBERS + Did you know that 2 is the only even number that is prime?")
else:
    print(x, " is odd.")
    print("FUN FACT ABOUT ODD NUMBERS + Did you know that multiplying two odd numbers " +
                                         "always gives an odd result?")
